using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace CleanCode
{
    class Calculator
    {
        public int sub(int a, int b)
        {
            return a - b;
        }
        int div(int a, int b)
        {
            return a / b;
        }

        double div(double a, double b)
        {
            return a / b;
        }
    }
}
